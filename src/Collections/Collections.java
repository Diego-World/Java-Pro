package Collections;

public class Collections {

        /* Diferente do array o tamanho de uma collection é dinâmico, e podem ter diversos tipos em uma collection
        * ainda que uma boa prática seja ter collections do mesmo tipo. As collections não suportam tipos primitos
        * ou usa-se Arrays ou Wrappers (String, Integer, BigDecimal e etc...).
        */

        /*
        *
        *                           SET:
        * - Não odernado (sorted set é ordernado, mas por padrão não.)
        * - Não indexado (Não possuí indice)
        * - Não aceita repetição (Valores repetidos são ignorados)
        *
        *                           LIST:
        * - Indexada
        * - Aceita repetição
        *
        *                           MAP:
        * - Chave/Valor
        * - Chave não aceita repetição
        * - Valor aceita repetição
        *
        *                           QUEUE:
        * - Implementa fila
        * - First in first out (Fifo)
        *
        *                           STACK:
        * - Implementa pilha
        * - Last in first out (Lifo)
        *
        * */
}
